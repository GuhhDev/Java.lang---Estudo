package br.com.aulas.Aula06;

public class ExemploLength {

	public static void main(String[] args) {
		
		/* Muitas vezes iremos precisar da contagem de algumas strings,
		 * Tendo que usar o metodo .Length() para trazer quantos caracteres possuem
		 * a String.
		 */
		
		// Criando variável para testes:
		String variavelTeste = "Alura";
		
		// Iremos verificar quantos caracteres possui a "variavelTeste"
		System.out.println(variavelTeste.length());
	}
}
